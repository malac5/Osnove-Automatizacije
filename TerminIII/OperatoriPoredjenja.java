package TerminIII;

public class OperatoriPoredjenja {
    public static void main(String[] args) {
// Operator ==
    int x=10;
    int y=20;
    boolean res= x==y;
        System.out.println(res);

// Operator <
    boolean res1= x<y;
        System.out.println("x  lower than y: "+res1);

// Operator >
    boolean res2=x>y;
        System.out.println("x greater than y: "+res2);

// Operator <=
    boolean res3=x<=y;
        System.out.println("x lower or equals y: " +res3);

// Operator >=
    boolean res4=x>=y;
        System.out.println("x greater or equals y: " +res4);

    }
}
